-- =====================================================
-- 기본 테이블 생성
-- =====================================================

CREATE TABLE users (
    id INT PRIMARY KEY,
    name VARCHAR(50),
    email VARCHAR(100),
    birth_date DATE,
    created_at DATETIME,
    utm_source VARCHAR(50),
    grade VARCHAR(20),
    phone VARCHAR(20),
    rfm_segment VARCHAR(20)
);

CREATE TABLE stores (
    id INT PRIMARY KEY,
    name VARCHAR(100),
    region VARCHAR(50),
    opened_at DATE
);

CREATE TABLE products (
    id INT PRIMARY KEY,
    name VARCHAR(100),
    category VARCHAR(50),
    category_id INT,
    size VARCHAR(20),
    price INT
);

CREATE TABLE orders (
    id INT PRIMARY KEY,
    user_id INT,
    store_id INT,
    order_datetime DATETIME,
    order_date DATE,
    total_amount INT,
    status VARCHAR(20)
);

CREATE TABLE order_items (
    id INT PRIMARY KEY,
    order_id INT,
    product_id INT,
    quantity INT,
    unit_price INT
);

CREATE TABLE app_logs (
    id INT PRIMARY KEY,
    user_id INT,
    event_name VARCHAR(50),
    event_date DATE,
    event_time DATETIME
);

CREATE TABLE marketing_costs (
    id INT PRIMARY KEY,
    ym VARCHAR(7),
    channel VARCHAR(50),
    cost INT
);

CREATE TABLE campaigns (
    id INT PRIMARY KEY,
    name VARCHAR(100),
    target_segment VARCHAR(50),
    budget INT,
    sent_at DATETIME
);

CREATE TABLE campaign_sends (
    id INT PRIMARY KEY,
    campaign_id INT,
    user_id INT,
    sent_at DATETIME
);

CREATE TABLE campaign_opens (
    id INT PRIMARY KEY,
    send_id INT,
    opened_at DATETIME
);

CREATE TABLE campaign_clicks (
    id INT PRIMARY KEY,
    send_id INT,
    clicked_at DATETIME
);

